package com.example.noteplus.models;

import androidx.room.Dao;
import androidx.room.PrimaryKey;

@Dao
class Folder {
    @PrimaryKey
    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
